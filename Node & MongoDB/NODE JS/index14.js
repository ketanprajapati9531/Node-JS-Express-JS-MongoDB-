/*
nodemon is a tool that helps develop Node.js based applications by automatically 
restarting the node application when file changes in the directory are detected.
nodemon does not require any additional changes to your code or method of development.
nodemon is a replacement wrapper for node. To use nodemon, replace the word node on the 
command line when executing your script.
npm install -g nodemon
*/

/*
After installing the module you can check the current version of the module by typing on 
console as shown below:

nodemon version

Usage:

The nodemon wraps your application, so you can pass all the arguments you would normally
pass to your app:

nodemon [your node app]
Options available for nodemon are shown below:
nodemon -h


Steps to run the program: Use the following command to run the file as shown below:

nodemon index.js
*/